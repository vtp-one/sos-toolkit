from typing import Optional, Dict, List, Union, Callable
from pydantic import BaseModel, Field

from sos_toolkit.meta._model import ModelGet, ModelDict, ModelParams

class ResultData(ModelDict):
    pass

class ResultObject(ModelGet):
    called_tool: Optional[str] = Field(default=None, description="The tool used")
    called_params: Optional[ModelParams] = Field(default=ModelParams(), description="The parameters used for the action")
    data: Optional[ResultData] = Field(default=ResultData(), description="The data returned from the tool")
    callbacks: Optional[List[ModelDict]] = Field(default=[], description="The callbacks generated by the tool")

class ResultRepo(ModelDict):
    pass